const sectionCenter = document.querySelector('.section-center');
const filterBtns = document.querySelectorAll('.filter-btn');

//load items
window.addEventListener('DOMContentLoaded', () => {
  displayMenuItems(menu);
});

//filter items
filterBtns.forEach((btn) => {
  btn.addEventListener('click', (e) => {
    const category = e.currentTarget.dataset.id;
    const menuCategory = menu.filter((menuItem) => {
      //console.log(menuItem.category)
    if(menuItem.category === category) {
      return menuItem;
    }
    });
    //console.log(menuCategory)
  })
})

function displayMenuItems(menuItems){
  let displayMenu = menuItems.map((item) => {
      return `<article class="menu-item">
                <img src=${item.img} class="photo" alt=${item.title}>
                <div class="item-info">
                  <h4 class="item-name">${item.title}</h4>
                  <button class="order-button">Order Now</button>
                </div>
              </article>`;
  });
    displayMenu = displayMenu.join('')
    sectionCenter.innerHTML = displayMenu;
}


//MOBILE NAVBAR
const navSlide = () => {
  const burger = document.querySelector('.burger');
  const nav1 = document.querySelector('.nav-links1');
  const navLinks1 = document.querySelectorAll('.nav-links1 li');
  
  burger.addEventListener('click', () => {
    //Toggle Nav
    nav1.classList.toggle('nav-active');

  // Animate Links Dynamically
  navLinks1.forEach((link, index) => {
    if(link.style.animation) {
      link.style.animation = ''
    } else {
      link.style.animation = `navLinkFade 0.5s ease forwards ${index / 10 + 0.4}s`;
      }
    });
    //Burger Animation
  burger.classList.toggle('toggle')
  }); 
};
// All Functions
const app = () => {
  navSlide();
};
// Envoking All Functions
app();
